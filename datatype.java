class datatype {
    // boolean data type
    public static void main(String[] args) {
        // boolean data type
        boolean flag = true;
        System.out.println(flag);

        byte range;
        range = 124;
        System.out.println(range);

        short temperature;
        temperature = -200;
        System.out.println(temperature); // prints -200

        int integer = -4250000;
        System.out.println(integer); // print -4250000

        long longtype = -42332200000L;
        System.out.println(longtype); // prints -42332200000

        double number = -42.3;
        System.out.println(number); // prints -42.3

        float floatnumber = -42.3f;
        System.out.println(floatnumber); // prints -42.3

        char letter = '\u0051';
        System.out.println(letter); // prints Q

        char letter1 = '9';
        System.out.println(letter1); // prints 9

        char letter2 = 65;
        System.out.println(letter2); // prints A

        String myString = "Java Programming";
        System.out.println(myString);
    }

}
